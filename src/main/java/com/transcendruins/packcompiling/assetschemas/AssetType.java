package com.transcendruins.packcompiling.assetschemas;

/**
 * <code>AssetType</code>: An enum class representing each asset type.
 */
public enum AssetType {

    /**
     * <code>AssetType</code>: An enum representing the <code>animation controller</code> type.
     */
    ANIMATION_CONTROLLER,

    /**
     * <code>AssetType</code>: An enum representing the <code>animation</code> type.
     */
    ANIMATION,

    /**
     * <code>AssetType</code>: An enum representing the <code>element</code> type.
     */
    ELEMENT,

    /**
     * <code>AssetType</code>: An enum representing the <code>entity</code> type.
     */
    ENTITY,

    /**
     * <code>AssetType</code>: An enum representing the <code>item</code> type.
     */
    ITEM,

    /**
     * <code>AssetType</code>: An enum representing the <code>model</code> type.
     */
    MODEL,

    /**
     * <code>AssetType</code>: An enum representing the <code>render material</code> type.
     */
    RENDER_MATERIAL,

    /**
     * <code>AssetType</code>: An enum representing the <code>structure</code> type.
     */
    STRUCTURE
}
